def _cluster_components_by_context_overlap(
    components: list[dict[str, Any]],
    image_width: int,
    image_height: int,
    context_pixels: int,
    multiple: int,
) -> list[dict[str, Any]]:
    """Merge disconnected mask fragments whose context crops overlap.

    Generation crops may overlap even when their owned target pixels do not.
    Planning those fragments independently produces redundant or fully nested
    tiles.  Clustering before cap-aware splitting lets one coherent edit tile
    own all nearby fragments whenever the selected tile profile can fit them.
    """
    if len(components) <= 1:
        return components

    crops: list[dict[str, int]] = []
    for component in components:
        crop = _make_crop(
            component["xs"],
            component["ys"],
            image_width,
            image_height,
            context_pixels,
            multiple,
        )
        if crop is None:
            raise ValueError("Unable to construct a context crop for a mask component")
        crops.append(crop)

    parents = list(range(len(components)))

    def find(index: int) -> int:
        while parents[index] != index:
            parents[index] = parents[parents[index]]
            index = parents[index]
        return index

    def union(left: int, right: int) -> None:
        left_root = find(left)
        right_root = find(right)
        if left_root != right_root:
            parents[right_root] = left_root

    ordered = sorted(range(len(crops)), key=lambda index: crops[index]["x"])
    active: list[int] = []
    for index in ordered:
        crop = crops[index]
        left = crop["x"]
        top = crop["y"]
        right = left + crop["width"]
        bottom = top + crop["height"]
        active = [
            other
            for other in active
            if crops[other]["x"] + crops[other]["width"] > left
        ]
        for other in active:
            other_crop = crops[other]
            other_top = other_crop["y"]
            other_bottom = other_top + other_crop["height"]
            if max(top, other_top) < min(bottom, other_bottom):
                union(index, other)
        active.append(index)

    grouped: dict[int, list[int]] = {}
    for index in range(len(components)):
        grouped.setdefault(find(index), []).append(index)

    clusters: list[dict[str, Any]] = []
    for indexes in grouped.values():
        if len(indexes) == 1:
            component = dict(components[indexes[0]])
            component["source_component_labels"] = [component["component_id"]]
            clusters.append(component)
            continue
        xs = np.concatenate([components[index]["xs"] for index in indexes])
        ys = np.concatenate([components[index]["ys"] for index in indexes])
        labels = [int(components[index]["component_id"]) for index in indexes]
        clusters.append(
            {
                "component_id": labels[0],
                "source_component_labels": labels,
                "xs": xs,
                "ys": ys,
                "bbox": [
                    int(xs.min()),
                    int(ys.min()),
                    int(xs.max()) + 1,
                    int(ys.max()) + 1,
                ],
                "pixels": int(xs.size),
            }
        )

    clusters.sort(key=lambda item: (item["bbox"][1], item["bbox"][0]))
    return clusters


def _fits_caps(
    width: int,
    height: int,
    max_long_side: int,
    max_short_side: int,
    max_pixels: int,
) -> bool:
    long_side = max(width, height)
    short_side = min(width, height)
    return (
        long_side <= max_long_side
        and short_side <= max_short_side
        and width * height <= max_pixels
    )


def _choose_split_axis(
    xs: np.ndarray,
    ys: np.ndarray,
    context_pixels: int,
    max_long_side: int,
    max_short_side: int,
) -> int:
    width = int(xs.max() - xs.min() + 1) + context_pixels * 2
    height = int(ys.max() - ys.min() + 1) + context_pixels * 2
    if width >= height:
        overflow_x = width / max_long_side
        overflow_y = height / max_short_side
    else:
        overflow_x = width / max_short_side
        overflow_y = height / max_long_side
    if abs(overflow_x - overflow_y) < 1e-9:
        return 1 if width >= height else 0
    return 1 if overflow_x > overflow_y else 0


def _choose_cut(values: np.ndarray, min_target_extent: int) -> int:
    lower = int(values.min())
    upper = int(values.max()) + 1
    if upper - lower <= 1:
        raise ValueError("Unable to split a one-pixel target extent")

    histogram = np.bincount(values - lower, minlength=upper - lower)
    cumulative = np.cumsum(histogram)
    total = int(values.size)
    max_hist = max(1, int(histogram.max()))

    candidates: list[tuple[float, int]] = []
    for cut in range(lower + 1, upper):
        left_count = int(cumulative[cut - lower - 1])
        right_count = total - left_count
        if left_count == 0 or right_count == 0:
            continue
        balance = min(left_count, right_count) / total
        if balance < 0.30:
            continue
        if cut - lower < min_target_extent or upper - cut < min_target_extent:
            continue

        seam_index = cut - lower
        seam_load = int(histogram[seam_index - 1])
        if seam_index < len(histogram):
            seam_load += int(histogram[seam_index])
        balance_penalty = abs(left_count - right_count) / total
        score = seam_load / max_hist + balance_penalty * 0.35
        candidates.append((score, cut))

    if candidates:
        candidates.sort(key=lambda item: (item[0], abs(item[1] - (lower + upper) / 2.0)))
        return candidates[0][1]

    ordered = np.sort(values)
    cut = int(ordered[len(ordered) // 2])
    if cut <= lower:
        cut = lower + 1
    if cut >= upper:
        cut = upper - 1
    return cut


def _choose_balanced_two_tile_cut(
    xs: np.ndarray,
    ys: np.ndarray,
    axis: int,
    image_width: int,
    image_height: int,
    max_long_side: int,
    max_short_side: int,
    max_pixels: int,
    context_pixels: int,
    multiple: int,
    min_target_extent: int,
) -> int | None:
    """Return a balanced cut only when both resulting context crops fit.

    This is the preferred split for a region that is only slightly over the
    selected profile.  It guarantees that the region becomes exactly two
    useful tiles instead of filling one tile as much as possible and leaving
    a tiny remainder tile.
    """
    values = xs if axis == 1 else ys
    other = ys if axis == 1 else xs
    order = np.argsort(values, kind="stable")
    sorted_values = values[order]
    sorted_other = other[order]
    if int(sorted_values[0]) == int(sorted_values[-1]):
        return None

    prefix_other_min = np.minimum.accumulate(sorted_other)
    prefix_other_max = np.maximum.accumulate(sorted_other)
    suffix_other_min = np.minimum.accumulate(sorted_other[::-1])[::-1]
    suffix_other_max = np.maximum.accumulate(sorted_other[::-1])[::-1]
    boundaries = np.nonzero(sorted_values[1:] != sorted_values[:-1])[0]
    if boundaries.size == 0:
        return None

    total_span = int(sorted_values[-1] - sorted_values[0] + 1)
    require_min_extent = total_span >= min_target_extent * 2
    lower = int(sorted_values[0])
    upper = int(sorted_values[-1]) + 1
    histogram = np.bincount(values - lower, minlength=upper - lower)
    max_hist = max(1, int(histogram.max()))
    total_pixels = int(values.size)
    candidates: list[tuple[float, float, int]] = []
    for boundary in boundaries:
        cut = int(sorted_values[boundary + 1])
        left_span = cut - lower
        right_span = upper - cut
        if require_min_extent and (
            left_span < min_target_extent or right_span < min_target_extent
        ):
            continue

        if axis == 1:
            left_bounds = (
                lower,
                int(prefix_other_min[boundary]),
                int(sorted_values[boundary]) + 1,
                int(prefix_other_max[boundary]) + 1,
            )
            right_bounds = (
                cut,
                int(suffix_other_min[boundary + 1]),
                upper,
                int(suffix_other_max[boundary + 1]) + 1,
            )
        else:
            left_bounds = (
                int(prefix_other_min[boundary]),
                lower,
                int(prefix_other_max[boundary]) + 1,
                int(sorted_values[boundary]) + 1,
            )
            right_bounds = (
                int(suffix_other_min[boundary + 1]),
                cut,
                int(suffix_other_max[boundary + 1]) + 1,
                upper,
            )

        left_crop = _make_crop_from_bounds(
            *left_bounds,
            image_width,
            image_height,
            context_pixels,
            multiple,
        )
        right_crop = _make_crop_from_bounds(
            *right_bounds,
            image_width,
            image_height,
            context_pixels,
            multiple,
        )
        if left_crop is None or right_crop is None:
            continue
        if not _fits_caps(
            left_crop["width"],
            left_crop["height"],
            max_long_side,
            max_short_side,
            max_pixels,
        ) or not _fits_caps(
            right_crop["width"],
            right_crop["height"],
            max_long_side,
            max_short_side,
            max_pixels,
        ):
            continue

        left_pixels = int(boundary + 1)
        right_pixels = total_pixels - left_pixels
        seam_index = cut - lower
        seam_load = int(histogram[seam_index - 1])
        if seam_index < len(histogram):
            seam_load += int(histogram[seam_index])
        left_area = left_crop["width"] * left_crop["height"]
        right_area = right_crop["width"] * right_crop["height"]
        area_balance = abs(left_area - right_area) / max(left_area, right_area)
        extent_balance = abs(left_span - right_span) / total_span
        pixel_balance = abs(left_pixels - right_pixels) / total_pixels
        seam_penalty = seam_load / max_hist
        score = (
            area_balance * 0.40
            + extent_balance * 0.30
            + pixel_balance * 0.15
            + seam_penalty * 0.15
        )
        candidates.append((score, abs(cut - (lower + upper) / 2.0), cut))

    if not candidates:
        return None

    candidates.sort(key=lambda item: (item[0], item[1]))
    return candidates[0][2]


def _sort_leaves_along_principal_axis(
    leaves: list[tuple[np.ndarray, np.ndarray, dict[str, int], int]],
    component_xs: np.ndarray,
    component_ys: np.ndarray,
) -> list[tuple[np.ndarray, np.ndarray, dict[str, int], int]]:
    centered_x = component_xs.astype(np.float64) - float(component_xs.mean())
    centered_y = component_ys.astype(np.float64) - float(component_ys.mean())
    covariance = np.array(
        [
            [np.mean(centered_x * centered_x), np.mean(centered_x * centered_y)],
            [np.mean(centered_x * centered_y), np.mean(centered_y * centered_y)],
        ],
        dtype=np.float64,
    )
    _, eigenvectors = np.linalg.eigh(covariance)
    direction = eigenvectors[:, -1]
    dominant = int(np.argmax(np.abs(direction)))
    if direction[dominant] < 0:
        direction = -direction

    return sorted(
        leaves,
        key=lambda leaf: float(leaf[0].mean()) * direction[0]
        + float(leaf[1].mean()) * direction[1],
    )


def _split_component(
    xs: np.ndarray,
    ys: np.ndarray,
    image_width: int,
    image_height: int,
    max_long_side: int,
    max_short_side: int,
    max_pixels: int,
    context_pixels: int,
    multiple: int,
    min_target_extent: int,
    depth: int = 0,
) -> list[tuple[np.ndarray, np.ndarray, dict[str, int], int]]:
    crop = _make_crop(xs, ys, image_width, image_height, context_pixels, multiple)
    if crop is not None and _fits_caps(
        crop["width"], crop["height"], max_long_side, max_short_side, max_pixels
    ):
        return [(xs, ys, crop, depth)]

    if depth >= 64:
        raise ValueError("Tile recursion exceeded 64 levels")

    primary_axis = _choose_split_axis(
        xs, ys, context_pixels, max_long_side, max_short_side
    )
    axes = (primary_axis, 1 - primary_axis)
    for axis in axes:
        values = xs if axis == 1 else ys
        if int(values.max()) == int(values.min()):
            continue
        cut = _choose_balanced_two_tile_cut(
            xs,
            ys,
            axis,
            image_width,
            image_height,
            max_long_side,
            max_short_side,
            max_pixels,
            context_pixels,
            multiple,
            min_target_extent,
        )
        if cut is None:
            continue
        left = values < cut
        right = ~left
        if not left.any() or not right.any():
            continue
        return _split_component(
            xs[left],
            ys[left],
            image_width,
            image_height,
            max_long_side,
            max_short_side,
            max_pixels,
            context_pixels,
            multiple,
            min_target_extent,
            depth + 1,
        ) + _split_component(
            xs[right],
            ys[right],
            image_width,
            image_height,
            max_long_side,
            max_short_side,
            max_pixels,
            context_pixels,
            multiple,
            min_target_extent,
            depth + 1,
        )

    for axis in axes:
        values = xs if axis == 1 else ys
        if int(values.max()) == int(values.min()):
            continue
        cut = _choose_cut(values, min_target_extent)
        left = values < cut
        right = ~left
        if not left.any() or not right.any():
            continue
        return _split_component(
            xs[left],
            ys[left],
            image_width,
            image_height,
            max_long_side,
            max_short_side,
            max_pixels,
            context_pixels,
            multiple,
            min_target_extent,
            depth + 1,
        ) + _split_component(
            xs[right],
            ys[right],
            image_width,
            image_height,
            max_long_side,
            max_short_side,
            max_pixels,
            context_pixels,
            multiple,
            min_target_extent,
            depth + 1,
        )

    raise ValueError(
        "Target cannot fit the configured tile caps while retaining the requested context"
    )


def _crops_overlap(left: dict[str, int], right: dict[str, int]) -> bool:
    return (
        max(left["x"], right["x"])
        < min(left["x"] + left["width"], right["x"] + right["width"])
        and max(left["y"], right["y"])
        < min(left["y"] + left["height"], right["y"] + right["height"])
    )


def _merge_compatible_leaves(
    leaves: list[dict[str, Any]],
    image_width: int,
    image_height: int,
    max_long_side: int,
    max_short_side: int,
    max_pixels: int,
    context_pixels: int,
    multiple: int,
) -> list[dict[str, Any]]:
    """Merge nearby split leaves whenever their joint crop still fits.

    Oversized connected components are split first.  This pass then absorbs
    nearby disconnected fragments into those already-safe leaves instead of
    binding every nearby component into one oversized cluster and cutting the
    combined geometry inefficiently.
    """
    groups = [dict(leaf) for leaf in leaves]
    while True:
        best: tuple[tuple[int, int, int, int, int], int, int, dict[str, Any]] | None = None
        for left_index in range(len(groups)):
            left = groups[left_index]
            for right_index in range(left_index + 1, len(groups)):
                right = groups[right_index]
                if not _crops_overlap(left["crop"], right["crop"]):
                    continue

                merged_xs = np.concatenate((left["xs"], right["xs"]))
                merged_ys = np.concatenate((left["ys"], right["ys"]))
                merged_crop = _make_crop(
                    merged_xs,
                    merged_ys,
                    image_width,
                    image_height,
                    context_pixels,
                    multiple,
                )
                if merged_crop is None or not _fits_caps(
                    merged_crop["width"],
                    merged_crop["height"],
                    max_long_side,
                    max_short_side,
                    max_pixels,
                ):
                    continue

                left_crop = left["crop"]
                right_crop = right["crop"]
                overlap_width = max(
                    0,
                    min(
                        left_crop["x"] + left_crop["width"],
                        right_crop["x"] + right_crop["width"],
                    )
                    - max(left_crop["x"], right_crop["x"]),
                )
                overlap_height = max(
                    0,
                    min(
                        left_crop["y"] + left_crop["height"],
                        right_crop["y"] + right_crop["height"],
                    )
                    - max(left_crop["y"], right_crop["y"]),
                )
                overlap_area = overlap_width * overlap_height
                separate_union_area = (
                    left_crop["width"] * left_crop["height"]
                    + right_crop["width"] * right_crop["height"]
                    - overlap_area
                )
                merged_area = merged_crop["width"] * merged_crop["height"]
                added_empty_area = merged_area - separate_union_area
                score = (
                    added_empty_area,
                    merged_area,
                    -overlap_area,
                    left_index,
                    right_index,
                )
                merged_group = {
                    "xs": merged_xs,
                    "ys": merged_ys,
                    "crop": merged_crop,
                    "depth": max(int(left["depth"]), int(right["depth"])),
                    "source_component_labels": sorted(
                        set(left["source_component_labels"])
                        | set(right["source_component_labels"])
                    ),
                }
                candidate = (score, left_index, right_index, merged_group)
                if best is None or candidate[0] < best[0]:
                    best = candidate

        if best is None:
            return groups

        _, left_index, right_index, merged_group = best
        groups[left_index] = merged_group
        groups.pop(right_index)


def _leaf_plan_score(leaves: list[dict[str, Any]]) -> tuple[int, float, int]:
    areas = [leaf["crop"]["width"] * leaf["crop"]["height"] for leaf in leaves]
    balance_penalty = 0.0 if len(areas) <= 1 else 1.0 - min(areas) / max(areas)
    return len(leaves), balance_penalty, sum(areas)


def build_region_tile_plan(
    mask: torch.Tensor | np.ndarray,
    max_long_side: int = 1536,
    max_short_side: int = 1024,
    max_pixels: int = 1572864,
    context_pixels: int = 192,
    multiple: int = 16,
    support_threshold: float = 0.001,
    min_component_pixels: int = 1,
    min_target_extent: int = 256,
) -> dict[str, Any]:
    if multiple <= 0:
        raise ValueError("multiple must be positive")
    if max_long_side < max_short_side:
        raise ValueError("max_long_side must be greater than or equal to max_short_side")
    if max_long_side % multiple or max_short_side % multiple:
        raise ValueError("tile side caps must be divisible by multiple")
    if context_pixels < 0:
        raise ValueError("context_pixels cannot be negative")

    source = _to_numpy_mask(mask)
    height, width = source.shape
    sanitized = source.copy()
    sanitized[sanitized < support_threshold] = 0.0
    active = sanitized > 0.0
    if not active.any():
        raise ValueError("Mask contains no active pixels at the configured support_threshold")

    structure = ndimage.generate_binary_structure(2, 2)
    labels, component_count_raw = ndimage.label(active, structure=structure)
    object_slices = ndimage.find_objects(labels)

    component_records: list[dict[str, Any]] = []
    dropped_pixels = 0
    for component_id, obj_slice in enumerate(object_slices, start=1):
        if obj_slice is None:
            continue
        local = labels[obj_slice] == component_id
        local_y, local_x = np.nonzero(local)
        pixel_count = int(local_x.size)
        if pixel_count < min_component_pixels:
            dropped_pixels += pixel_count
            continue
        global_y = local_y + obj_slice[0].start
        global_x = local_x + obj_slice[1].start
        component_records.append(
            {
                "component_id": component_id,
                "xs": global_x.astype(np.int32, copy=False),
                "ys": global_y.astype(np.int32, copy=False),
                "bbox": [
                    int(global_x.min()),
                    int(global_y.min()),
                    int(global_x.max()) + 1,
                    int(global_y.max()) + 1,
                ],
                "pixels": pixel_count,
            }
        )

    if not component_records:
        raise ValueError("All mask components were smaller than min_component_pixels")

    component_records.sort(key=lambda item: (item["bbox"][1], item["bbox"][0]))
    clustered_records = _cluster_components_by_context_overlap(
        component_records,
        width,
        height,
        context_pixels,
        multiple,
    )
    planned_target = np.zeros_like(sanitized)
    tiles: list[dict[str, Any]] = []
    component_summaries: list[dict[str, Any]] = []

    components_by_label = {
        int(component["component_id"]): component for component in component_records
    }

    for ordered_component_id, component in enumerate(clustered_records):
        xs = component["xs"]
        ys = component["ys"]
        atomic_leaves: list[dict[str, Any]] = []
        source_labels = component.get(
            "source_component_labels", [component["component_id"]]
        )
        for source_label in source_labels:
            source_component = components_by_label[int(source_label)]
            source_leaves = _split_component(
                source_component["xs"],
                source_component["ys"],
                width,
                height,
                max_long_side,
                max_short_side,
                max_pixels,
                context_pixels,
                multiple,
                min_target_extent,
            )
            for leaf_x, leaf_y, crop, depth in source_leaves:
                atomic_leaves.append(
                    {
                        "xs": leaf_x,
                        "ys": leaf_y,
                        "crop": crop,
                        "depth": depth,
                        "source_component_labels": [int(source_label)],
                    }
                )

        split_then_merge_leaves = _merge_compatible_leaves(
            atomic_leaves,
            width,
            height,
            max_long_side,
            max_short_side,
            max_pixels,
            context_pixels,
            multiple,
        )
        combined_leaves = [
            {
                "xs": leaf_x,
                "ys": leaf_y,
                "crop": crop,
                "depth": depth,
                "source_component_labels": [int(label) for label in source_labels],
            }
            for leaf_x, leaf_y, crop, depth in _split_component(
                xs,
                ys,
                width,
                height,
                max_long_side,
                max_short_side,
                max_pixels,
                context_pixels,
                multiple,
                min_target_extent,
            )
        ]
        merged_leaves = min(
            (combined_leaves, split_then_merge_leaves),
            key=_leaf_plan_score,
        )
        leaf_lookup = {
            (id(leaf["xs"]), id(leaf["ys"])): leaf for leaf in merged_leaves
        }
        ordered_tuples = _sort_leaves_along_principal_axis(
            [
                (leaf["xs"], leaf["ys"], leaf["crop"], int(leaf["depth"]))
                for leaf in merged_leaves
            ],
            xs,
            ys,
        )
        leaves = [
            leaf_lookup[(id(leaf_x), id(leaf_y))]
            for leaf_x, leaf_y, _, _ in ordered_tuples
        ]

        component_tile_indexes: list[int] = []
        for component_tile_index, leaf in enumerate(leaves):
            leaf_x = leaf["xs"]
            leaf_y = leaf["ys"]
            crop = leaf["crop"]
            depth = int(leaf["depth"])
            x = crop["x"]
            y = crop["y"]
            tile_width = crop["width"]
            tile_height = crop["height"]
            ownership_mask = np.zeros((tile_height, tile_width), dtype=np.float32)
            ownership_mask[leaf_y - y, leaf_x - x] = sanitized[leaf_y, leaf_x]
            planned_target[leaf_y, leaf_x] = sanitized[leaf_y, leaf_x]

            tile_index = len(tiles)
            component_tile_indexes.append(tile_index)
            tiles.append(
                {
                    "tile_index": tile_index,
                    "component_id": ordered_component_id,
                    "source_component_label": leaf["source_component_labels"][0],
                    "source_component_labels": leaf["source_component_labels"],
                    "component_tile_index": component_tile_index,
                    "x": x,
                    "y": y,
                    "width": tile_width,
                    "height": tile_height,
                    "input_pixels": tile_width * tile_height,
                    "ownership_pixels": int(leaf_x.size),
                    "target_bbox_xyxy": crop["target_bbox_xyxy"],
                    "split_depth": depth,
                    "ownership_mask": ownership_mask,
                }
            )

        component_summaries.append(
            {
                "component_id": ordered_component_id,
                "source_component_label": component["component_id"],
                "source_component_labels": component.get(
                    "source_component_labels", [component["component_id"]]
                ),
                "bbox_xyxy": component["bbox"],
                "pixels": component["pixels"],
                "tile_indexes": component_tile_indexes,
            }
        )

    union = np.zeros_like(sanitized)
    ownership_count = np.zeros_like(active, dtype=np.uint16)
    for tile in tiles:
        x = tile["x"]
        y = tile["y"]
        tile_width = tile["width"]
        tile_height = tile["height"]
        ownership_mask = tile["ownership_mask"]
        region = union[y : y + tile_height, x : x + tile_width]
        np.maximum(region, ownership_mask, out=region)
        ownership_count[y : y + tile_height, x : x + tile_width] += (
            ownership_mask > 0
        ).astype(np.uint16)

    overlap_pixels = int(np.count_nonzero(ownership_count > 1))
    union_mismatch_pixels = int(np.count_nonzero(union != planned_target))
    if overlap_pixels != 0 or union_mismatch_pixels != 0:
        raise RuntimeError(
            f"Tile ownership invariant failed: overlap={overlap_pixels}, "
            f"union_mismatch={union_mismatch_pixels}"
        )

    report_tiles = [
        {key: value for key, value in tile.items() if key != "ownership_mask"}
        for tile in tiles
    ]
    report = {
        "image_width": width,
        "image_height": height,
        "support_threshold": support_threshold,
        "active_pixels": int(np.count_nonzero(planned_target)),
        "dropped_component_pixels": dropped_pixels,
        "raw_component_count": int(component_count_raw),
        "kept_component_count": len(component_records),
        "cluster_count": len(clustered_records),
        "tile_count": len(tiles),
        "caps": {
            "max_long_side": max_long_side,
            "max_short_side": max_short_side,
            "max_pixels": max_pixels,
            "context_pixels": context_pixels,
            "multiple": multiple,
            "min_target_extent": min_target_extent,
        },
        "ownership_overlap_pixels": overlap_pixels,
        "ownership_union_mismatch_pixels": union_mismatch_pixels,
        "components": component_summaries,
        "tiles": report_tiles,
    }

    return {
        "tiles": tiles,
        "count": len(tiles),
        "union_mask": union,
        "report": report,
    }


